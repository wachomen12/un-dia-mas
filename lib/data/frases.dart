  static String delDia(Categoria categoria, DateTime fecha) {
    final lista = _porCategoria[categoria]!;
    final dia = DateTime(fecha.year, fecha.month, fecha.day);
    final epoch = DateTime(2024, 1, 1);
    final diasDesde = dia.difference(epoch).inDays;
    final indice = diasDesde.abs() % lista.length;
    return lista[indice];
  }

  static String fraseCalma(DateTime fecha) {
    return delDia(Categoria.necesitoCalma, fecha);
  }

